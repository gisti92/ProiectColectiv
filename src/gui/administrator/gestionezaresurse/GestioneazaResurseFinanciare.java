    private void refreshTable() {
        try {
            List<ResursaFinanciara> list = contr.getResurseFinanciare();
            sumaInBd = contr.getSumaResurseFinanciare();

            txtSumaTotala.setText(String.valueOf(sumaInBd));

            Color green = new Color(50, 205, 50);
            Color red = new Color(255, 0, 0);
            Color black = new Color(0, 0, 0);

            if (sumaInBd < 0) {
                txtSumaTotala.setForeground(red);
            } else if (sumaInBd == 0) {
                txtSumaTotala.setForeground(black);
            } else {
                txtSumaTotala.setForeground(green);
            }
            sumaChanged();

            tableModel = new ResurseFinanciareTM(list);
            tableResurseFinanciare.setModel(tableModel);

        } catch (Exception ex) {
            Logger.getLogger(GestioneazaResurseFinanciare.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Eroare la incarcarea resurselor financiale", "Eroare", JOptionPane.ERROR_MESSAGE);
            dispose();
        }
    }

    private void initComboBoxModel() {
        cbTip.setModel(new DefaultComboBoxModel(new ResursaFinanciara.TipCheltuiala[]{
            null,
            ResursaFinanciara.TipCheltuiala.CHELTUIALA_CU_MANOPERA,
            ResursaFinanciara.TipCheltuiala.CHELTUIALA_DE_LOGISTICA,
            ResursaFinanciara.TipCheltuiala.CHELTUIELA_CU_MOBILITATE
        }));
    }

    private void bindEvents() {
        ListSelectionModel rowSelectionModel = tableResurseFinanciare.getSelectionModel();
        rowSelectionModel.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        rowSelectionModel.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                int selectedRow = tableResurseFinanciare.getSelectedRow();
                if (selectedRow >= 0) {
                    ResurseFinanciareTM model = (ResurseFinanciareTM) tableResurseFinanciare.getModel();
                    txtDescriere.setText((String) model.getValueAt(selectedRow, 0));
                    suma.setValue((int) model.getValueAt(selectedRow, 1));
                    cbTip.setSelectedItem(model.getTip(selectedRow));
                } else {
                    txtDescriere.setText("");
                    suma.setValue(0);
                    cbTip.setSelectedItem(null);
                }
            }
        });
    }

    private void sumaChanged() {
        Color green = new Color(50, 205, 50);
        Color red = new Color(255, 0, 0);
        Color black = new Color(0, 0, 0);

        int sumaNoua = sumaInBd + (int) suma.getValue();
        txtSumaTotalaDupa.setText(String.valueOf(sumaNoua));
        if (sumaNoua < 0) {
            txtSumaTotalaDupa.setForeground(red);
        } else if (sumaNoua == 0) {
            txtSumaTotalaDupa.setForeground(black);
        } else {
            txtSumaTotalaDupa.setForeground(green);
        }

        if ((int) suma.getValue() < 0) {
            btnIncaseazaSuma.setText("Adauga cheltuiala");
        } else {
            btnIncaseazaSuma.setText("Incaseaza suma");
        }
    }
