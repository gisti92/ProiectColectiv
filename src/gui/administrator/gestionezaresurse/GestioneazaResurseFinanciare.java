    private void btnIncaseazaSumaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncaseazaSumaActionPerformed
        if (txtDescriere.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Descrierea trebuie completata", "Eroare", JOptionPane.ERROR_MESSAGE);
            return;
        }
        ResursaFinanciara rf = new ResursaFinanciara();
        rf.setDescriere(txtDescriere.getText());
        int sumaInt = (int) suma.getValue();
        rf.setSuma(sumaInt);
        rf.setTip((ResursaFinanciara.TipCheltuiala) cbTip.getSelectedItem());
        try {
            contr.addResursaFinaciara(rf);
        } catch (Exception ex) {
            Logger.getLogger(GestioneazaResurseFinanciare.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Eroare la adaugare " + (sumaInt < 0 ? "cheltuiala" : "suma") + " ", "Eroare", JOptionPane.ERROR_MESSAGE);
        }
        refreshTable();
    }//GEN-LAST:event_btnIncaseazaSumaActionPerformed

