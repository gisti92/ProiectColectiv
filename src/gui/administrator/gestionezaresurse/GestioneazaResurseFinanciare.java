    private void btnStergeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStergeActionPerformed

        try {
            final int selectedRow = tableResurseFinanciare.getSelectedRow();
            if (selectedRow < 0) {
                JOptionPane.showMessageDialog(this, "Mai intai selectati o inregistrare", "Eroare", JOptionPane.ERROR_MESSAGE);
                return;
            }
            int result = JOptionPane.showConfirmDialog(this, "Sigur vreti sa efectuati stergerea? ", "?", JOptionPane.YES_NO_OPTION);
            if (result == JOptionPane.NO_OPTION) {
                return;
            }
            int id = tableModel.getId(selectedRow);
            contr.stergeResursaFinancara(id);
        } catch (Exception ex) {
            Logger.getLogger(GestioneazaResurseFinanciare.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Eroare la stergere", "Eroare", JOptionPane.ERROR_MESSAGE);
        }
        refreshTable();
    }//GEN-LAST:event_btnStergeActionPerformed

    private void btnModificaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificaActionPerformed
        final int selectedRow = tableResurseFinanciare.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Mai intai selectati o inregistrare", "Eroare", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int id = tableModel.getId(selectedRow);
        try {
            contr.modificaResursaFinanciara(id, txtDescriere.getText(), (int) suma.getValue(), (ResursaFinanciara.TipCheltuiala) cbTip.getSelectedItem());
        } catch (Exception ex) {
            Logger.getLogger(GestioneazaResurseFinanciare.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Eroare la modifcare", "Eroare", JOptionPane.ERROR_MESSAGE);
        }
        refreshTable();
        tableResurseFinanciare.setRowSelectionInterval(selectedRow, selectedRow);
    }//GEN-LAST:event_btnModificaActionPerformed
